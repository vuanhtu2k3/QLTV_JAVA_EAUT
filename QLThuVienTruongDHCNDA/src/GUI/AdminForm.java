    private void btnThemPhieuXLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemPhieuXLActionPerformed
        if (txtMaXuLy_XLQH.getText().trim().equals("")
            || txtTienPhat_XLQH.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin");
        } else {
            XuLyQuaHan tl = new XuLyQuaHan();
            try {
                ThemXuLy(tl);
            } catch (BadLocationException | ParseException ex) {
                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                if (!XuLyQuanHanController.Check(txtMaXuLy_XLQH.getText())) {
                    try {
                        XuLyQuanHanController.Them(tl);
                    } catch (SQLException ex) {
                        Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    try {
                        XuLyQuanHanController.HienThi();
                        HienThiXuLy(XuLyQuanHanController.List);
                    } catch (Exception ex) {
                        Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Đã tồn tại phiếu xử lý này này");
                }
            } catch (SQLException ex) {
                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnThemPhieuXLActionPerformed

