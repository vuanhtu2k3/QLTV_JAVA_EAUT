    private void btnLapPhieuMuonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLapPhieuMuonActionPerformed
        if (txtMaPhieu_QLMT.getText().trim().equals("")
            || txtMaSinhVien_QLMT.getText().trim().equals("")
            || txtHoVaTen_QLMT.getText().trim().equals("")
            || txtNgayMuon_QLMT.getText().trim().equals("")
            || txtNgayTra_QLMT.getText().trim().equals("")
            || txtNgayThucTra_QLMT.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin");
        } else {
            if (BanDocController.CheckTime(txtNgayMuon_QLMT.getText())
                && BanDocController.CheckTime(txtNgayTra_QLMT.getText())
                && BanDocController.CheckTime(txtNgayThucTra_QLMT.getText())) {
                Phieu tl = new Phieu();
                try {
                    ThemPhieu(tl);
                } catch (BadLocationException | ParseException ex) {
                    Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    if (!PhieuController.Check(txtMaPhieu_QLMT.getText())) // Kiểm tra tồn tại mã phiếu
                    {
                        if (PhieuController.CheckKhoaNgoai(tl.getMaTaiLieu(), tl.getMaBanDoc())) { // Kiểm tra tồn tại mã TL,BD
                            try {
                                PhieuController.ThemPhieu(tl);

                            } catch (SQLException ex) {
                                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);

                            }
                            try {
                                PhieuController.HienThi();
                                HienThiPhieu(PhieuController.List);
                            } catch (Exception ex) {
                                Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Mã bạn đọc hoặc mã tài liệu không tồn tại");
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Phiếu này đã tồn tại");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Sai định dạng ngày : yyyy-MM-dd");
            }

        }
    }//GEN-LAST:event_btnLapPhieuMuonActionPerformed

