    private void tblLoaisachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLoaisachMouseClicked
        // TODO add your handling code here:
        int index = tblLoaisach.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn hàng");
        } else {
            txtMatheloai.setText(TheloaiDAO.List.get(index).getMaTheLoai());
            txtTenTheloai.setText(TheloaiDAO.List.get(index).getTenTheLoai());
        }
    }//GEN-LAST:event_tblLoaisachMouseClicked

