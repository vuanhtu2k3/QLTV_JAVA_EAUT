    public void XoaTheLoai() throws SQLException, Exception {
        int n = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa ?", "Thông báo ", JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION) {
            int index = tblLoaisach.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn hàng để xóa");
            } else {
                
                    TheloaiDAO.XoaTheLoai(txtMatheloai.getText());
                    JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                    TheloaiDAO.HienThi();
                    HienTheloai(TheloaiDAO.List);
                

            }
        }

    }
     public void ThemTheLoai(TheLoai tl) {
         tl.setMaTheLoai(txtMatheloai.getText());
         tl.setTenTheLoai(txtTenTheloai.getText());
        
     }
