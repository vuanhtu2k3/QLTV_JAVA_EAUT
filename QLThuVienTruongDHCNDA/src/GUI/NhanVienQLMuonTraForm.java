    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
         switch (cbxDoiTuong.getSelectedItem().toString()) {
            case "Phiếu":

                TimKiem.setColumnIdentifiers(new Object[]{
                    "Mã phiếu", "Mã tài liệu", "Mã sinh viên", "Họ và tên", "Ngày mượn", "Ngày trả", "Ngày thực trả"
                });
                switch (cbxThuocTinh.getSelectedItem().toString()) {
                    case "Mã Phiếu":
                        try {
                            PhieuController.TimMaPhieu(txtTimKiem.getText());
                        } catch (Exception ex) {
                            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    case "Mã Sinh Viên":
                        try {
                            PhieuController.TimMaSV(txtTimKiem.getText());
                        } catch (Exception ex) {
                            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    case "Tên Sinh Viên":
                        try {
                            PhieuController.TimTenSV(txtTimKiem.getText());
                        } catch (Exception ex) {
                            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    case "Mã Tài liệu":
                        try {
                            PhieuController.TimMaTaiLieu(txtTimKiem.getText());
                        } catch (Exception ex) {
                            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;

                    default:
                        break;
                }

                HienThiPhieu1(PhieuController.List1);
                break;
            case "Xử lý quá hạn":

            TimKiem.setColumnIdentifiers(new Object[]{
                "Mã xử lý", "Mã phiếu", "Lý do", "Tiền phạt", "Trạng thái"
            });
            switch (cbxThuocTinh.getSelectedItem().toString()) {
                case "Mã xử lý":
                try {
                    XuLyQuanHanController.TimKiemMaXuLy(txtTimKiem.getText());
                } catch (Exception ex) {
                    Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
                case "Mã phiếu mượn":
                try {
                    XuLyQuanHanController.TimKiemMaPhieu(txtTimKiem.getText());
                } catch (Exception ex) {
                    Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;

                default:
                break;
            }

            HienThiXuLy1(XuLyQuanHanController.List1);
            break;
            case "Bạn đọc":

                TimKiem.setColumnIdentifiers(new Object[]{
                    "Mã bạn đọc", "Tên bạn đọc", "Ngày sinh", "Lớp - Khóa", "Số điện thoại" , "Giới tính"
                });
                switch (cbxThuocTinh.getSelectedItem().toString()) {
                    case "Mã bạn đọc":
                        try {
                            BanDocController.TimKiemMaSV(txtTimKiem.getText());
                        } catch (Exception ex) {
                            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    case "Tên bạn đọc":
                        try {
                            BanDocController.TimKiemTenSV(txtTimKiem.getText());
                        } catch (Exception ex) {
                            Logger.getLogger(AdminForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;

                    default:
                        break;
                }

                HienThiBanDoc1(BanDocController.List1);
                break;
                default:
                break;
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

