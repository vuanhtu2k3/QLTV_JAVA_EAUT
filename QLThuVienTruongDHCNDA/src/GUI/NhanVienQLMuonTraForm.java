    public void SuaPhieu() throws SQLException, Exception {
        int n = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn sửa ?", "Thông báo ", JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION) {
            int index = tblQuanLyMuonTra.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn hàng để sửa");
            } else {
                Phieu tl = new Phieu();
                ThemPhieu(tl);
                PhieuController.SuaPhieu(tl);

            }
            PhieuController.HienThi();
            HienThiPhieu(PhieuController.List);
        }
    }

    public void XoaPhieu() throws SQLException, Exception {
        int n = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa ?", "Thông báo ", JOptionPane.YES_NO_OPTION);
        if (n == JOptionPane.YES_OPTION) {
            int index = tblQuanLyMuonTra.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn hàng để xóa");
            } else {
                PhieuController.XoaPhieu(txtMaPhieu_QLMT.getText());
                JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                PhieuController.HienThi();
                HienThiPhieu(PhieuController.List);
            }

        }

    }

    
